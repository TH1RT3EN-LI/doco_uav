#!/usr/bin/env python3

import argparse
import ast
import math
import sys
from pathlib import Path
from typing import Any, Dict, List, Tuple


CAMERA_KEYS = ["cam0", "cam1"]


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Apply the last OpenVINS calibration estimate to a kalibr_imucam_chain YAML file."
    )
    parser.add_argument(
        "--state-estimate-path",
        default="/tmp/openvins_orbbec_calibration/ov_estimate.txt",
        help="Path to OpenVINS state output file generated by save_total_state.",
    )
    parser.add_argument(
        "--input-imucam-yaml",
        required=True,
        help="Input kalibr_imucam_chain.yaml path.",
    )
    parser.add_argument(
        "--output-imucam-yaml",
        default="",
        help="Optional output path. Defaults to overwriting the input file.",
    )
    parser.add_argument(
        "--update-intrinsics",
        action="store_true",
        help="Also overwrite intrinsics and distortion coefficients from OpenVINS online estimates.",
    )
    return parser.parse_args()


def parse_scalar_or_list(value: str) -> Any:
    return ast.literal_eval(value.strip())


def parse_imucam_yaml(path: Path) -> Dict[str, Dict[str, Any]]:
    cameras: Dict[str, Dict[str, Any]] = {}
    current_camera = None
    current_matrix_key = None

    for raw_line in path.read_text(encoding="utf-8").splitlines():
        stripped = raw_line.strip()
        if not stripped or stripped.startswith("%") or stripped.startswith("#"):
            continue

        if current_camera is not None and current_matrix_key is not None and stripped.startswith("-"):
            cameras[current_camera].setdefault(current_matrix_key, []).append(
                parse_scalar_or_list(stripped[1:].strip())
            )
            if len(cameras[current_camera][current_matrix_key]) == 4:
                current_matrix_key = None
            continue

        if stripped.endswith(":") and stripped.startswith("cam"):
            current_camera = stripped[:-1]
            cameras[current_camera] = {}
            current_matrix_key = None
            continue
        if current_camera is None or ":" not in stripped:
            continue

        key, value = stripped.split(":", 1)
        key = key.strip()
        value = value.strip()
        if not value:
            if key in {"T_cam_imu", "T_cn_cnm1"}:
                cameras[current_camera][key] = []
                current_matrix_key = key
            continue

        if key in {"cam_overlaps", "distortion_coeffs", "intrinsics", "resolution"}:
            cameras[current_camera][key] = parse_scalar_or_list(value)
        else:
            cameras[current_camera][key] = value

    return cameras


def parse_last_state_line(
    path: Path,
) -> Tuple[float, List[float], List[float], List[Dict[str, List[float]]]]:
    lines = [
        line.strip()
        for line in path.read_text(encoding="utf-8").splitlines()
        if line.strip() and not line.lstrip().startswith("#")
    ]
    if not lines:
        raise RuntimeError(f"state estimate file is empty: {path}")

    values = [float(token) for token in lines[-1].split()]
    if len(values) < 19:
        raise RuntimeError("state estimate line is too short")

    position = values[5:8]
    velocity = values[8:11]
    time_offset = values[17]
    num_cameras = int(round(values[18]))
    index = 19
    cameras = []

    for camera_index in range(num_cameras):
        if index + 15 > len(values):
            raise RuntimeError(
                f"state estimate camera block {camera_index} is incomplete"
            )
        intrinsics = values[index : index + 8]
        index += 8
        transform = values[index : index + 7]
        index += 7
        cameras.append({"intrinsics": intrinsics, "transform": transform})

    return time_offset, position, velocity, cameras


def transform_matrix_to_rt(matrix: List[List[float]]) -> Tuple[List[List[float]], List[float]]:
    rotation = [row[:3] for row in matrix[:3]]
    translation = [row[3] for row in matrix[:3]]
    return rotation, translation


def rotation_angle_deg(rotation: List[List[float]]) -> float:
    trace = rotation[0][0] + rotation[1][1] + rotation[2][2]
    cos_angle = max(-1.0, min(1.0, 0.5 * (trace - 1.0)))
    return math.degrees(math.acos(cos_angle))


def vector_norm(vector: List[float]) -> float:
    return math.sqrt(sum(value * value for value in vector))


def q_to_rotation(qx: float, qy: float, qz: float, qw: float) -> List[List[float]]:
    norm = math.sqrt(qx * qx + qy * qy + qz * qz + qw * qw)
    if norm <= 1e-12:
        raise RuntimeError("invalid zero-length quaternion in state estimate")

    qx /= norm
    qy /= norm
    qz /= norm
    qw /= norm

    xx = qx * qx
    yy = qy * qy
    zz = qz * qz
    ww = qw * qw
    xy = qx * qy
    xz = qx * qz
    yz = qy * qz
    wx = qw * qx
    wy = qw * qy
    wz = qw * qz

    return [
        [ww + xx - yy - zz, 2.0 * (xy + wz), 2.0 * (xz - wy)],
        [2.0 * (xy - wz), ww - xx + yy - zz, 2.0 * (yz + wx)],
        [2.0 * (xz + wy), 2.0 * (yz - wx), ww - xx - yy + zz],
    ]


def format_scalar(value: float) -> str:
    return f"{value:.12g}"


def format_list(values: List[float]) -> str:
    return "[" + ", ".join(format_scalar(value) for value in values) + "]"


def format_transform(rotation: List[List[float]], translation: List[float]) -> List[str]:
    return [
        f"    - [{format_scalar(rotation[0][0])}, {format_scalar(rotation[0][1])}, {format_scalar(rotation[0][2])}, {format_scalar(translation[0])}]",
        f"    - [{format_scalar(rotation[1][0])}, {format_scalar(rotation[1][1])}, {format_scalar(rotation[1][2])}, {format_scalar(translation[1])}]",
        f"    - [{format_scalar(rotation[2][0])}, {format_scalar(rotation[2][1])}, {format_scalar(rotation[2][2])}, {format_scalar(translation[2])}]",
        "    - [0.0, 0.0, 0.0, 1.0]",
    ]


def invert_transform(
    rotation: List[List[float]], translation: List[float]
) -> Tuple[List[List[float]], List[float]]:
    rot_t = [[rotation[col][row] for col in range(3)] for row in range(3)]
    trans = [
        -(
            rot_t[0][0] * translation[0]
            + rot_t[0][1] * translation[1]
            + rot_t[0][2] * translation[2]
        ),
        -(
            rot_t[1][0] * translation[0]
            + rot_t[1][1] * translation[1]
            + rot_t[1][2] * translation[2]
        ),
        -(
            rot_t[2][0] * translation[0]
            + rot_t[2][1] * translation[1]
            + rot_t[2][2] * translation[2]
        ),
    ]
    return rot_t, trans


def compose_transforms(
    a_rot: List[List[float]],
    a_trans: List[float],
    b_rot: List[List[float]],
    b_trans: List[float],
) -> Tuple[List[List[float]], List[float]]:
    rotation = [
        [sum(a_rot[i][k] * b_rot[k][j] for k in range(3)) for j in range(3)]
        for i in range(3)
    ]
    translation = [
        sum(a_rot[i][k] * b_trans[k] for k in range(3)) + a_trans[i]
        for i in range(3)
    ]
    return rotation, translation


def build_output_yaml(
    cameras_in: Dict[str, Dict[str, Any]],
    cameras_out: List[Dict[str, List[float]]],
    time_offset: float,
    update_intrinsics: bool,
) -> str:
    for camera_key in CAMERA_KEYS:
        if camera_key not in cameras_in:
            raise RuntimeError(
                f"input imucam yaml must contain {camera_key}"
            )
    if len(cameras_out) < len(CAMERA_KEYS):
        raise RuntimeError("state estimate must contain at least two cameras")

    out_blocks: List[str] = []
    parsed_transforms: List[Tuple[List[List[float]], List[float]]] = []

    for index, camera_key in enumerate(CAMERA_KEYS):
        qx, qy, qz, qw, px, py, pz = cameras_out[index]["transform"]
        rotation = q_to_rotation(qx, qy, qz, qw)
        translation = [px, py, pz]
        parsed_transforms.append((rotation, translation))

        template = cameras_in[camera_key]
        if update_intrinsics:
            intrinsics = cameras_out[index]["intrinsics"][:4]
            distortion_coeffs = cameras_out[index]["intrinsics"][4:8]
        else:
            intrinsics = [float(value) for value in template["intrinsics"]]
            distortion_coeffs = [float(value) for value in template["distortion_coeffs"]]

        block = [
            f"{camera_key}:",
            "  T_cam_imu:",
            *format_transform(rotation, translation),
        ]

        if camera_key == "cam1":
            cam1_rot, cam1_trans = rotation, translation
            cam0_rot, cam0_trans = parsed_transforms[0]
            cam0_inv_rot, cam0_inv_trans = invert_transform(cam0_rot, cam0_trans)
            stereo_rot, stereo_trans = compose_transforms(
                cam1_rot,
                cam1_trans,
                cam0_inv_rot,
                cam0_inv_trans,
            )
            block.extend(
                [
                    "  T_cn_cnm1:",
                    *format_transform(stereo_rot, stereo_trans),
                ]
            )

        block.extend(
            [
                f"  cam_overlaps: {template.get('cam_overlaps', [])}",
                f"  camera_model: {template.get('camera_model', 'pinhole')}",
                f"  distortion_coeffs: {format_list(distortion_coeffs)}",
                f"  distortion_model: {template.get('distortion_model', 'radtan')}",
                f"  intrinsics: {format_list(intrinsics)}",
                f"  resolution: [{int(template['resolution'][0])}, {int(template['resolution'][1])}]",
                f"  rostopic: {template['rostopic']}",
                f"  timeshift_cam_imu: {format_scalar(time_offset)}",
                "",
            ]
        )
        out_blocks.extend(block)

    return "%YAML:1.0\n\n" + "\n".join(out_blocks) + "\n"


def main() -> int:
    args = parse_args()
    input_path = Path(args.input_imucam_yaml).expanduser().resolve()
    output_path = (
        Path(args.output_imucam_yaml).expanduser().resolve()
        if args.output_imucam_yaml
        else input_path
    )
    state_path = Path(args.state_estimate_path).expanduser().resolve()

    cameras_in = parse_imucam_yaml(input_path)
    time_offset, final_position, final_velocity, cameras_out = parse_last_state_line(state_path)
    content = build_output_yaml(
        cameras_in,
        cameras_out,
        time_offset,
        args.update_intrinsics,
    )

    output_path.parent.mkdir(parents=True, exist_ok=True)
    output_path.write_text(content, encoding="utf-8")

    final_speed = vector_norm(final_velocity)
    final_pos_norm = vector_norm(final_position)

    qx0, qy0, qz0, qw0, px0, py0, pz0 = cameras_out[0]["transform"]
    qx1, qy1, qz1, qw1, px1, py1, pz1 = cameras_out[1]["transform"]
    cam0_rt = (q_to_rotation(qx0, qy0, qz0, qw0), [px0, py0, pz0])
    cam1_rt = (q_to_rotation(qx1, qy1, qz1, qw1), [px1, py1, pz1])
    cam0_inv = invert_transform(*cam0_rt)
    estimated_stereo_rt = compose_transforms(cam1_rt[0], cam1_rt[1], cam0_inv[0], cam0_inv[1])

    print(f"wrote {output_path}")
    print(f"applied camera-imu time offset: {time_offset:.7f}")
    print(f"last-state position norm: {final_pos_norm:.3f} m")
    print(f"last-state speed norm: {final_speed:.3f} m/s")

    if "T_cn_cnm1" in cameras_in["cam1"] and cameras_in["cam1"]["T_cn_cnm1"]:
        input_stereo_rt = transform_matrix_to_rt(cameras_in["cam1"]["T_cn_cnm1"])
        input_baseline = vector_norm(input_stereo_rt[1])
        estimated_baseline = vector_norm(estimated_stereo_rt[1])
        input_angle = rotation_angle_deg(input_stereo_rt[0])
        estimated_angle = rotation_angle_deg(estimated_stereo_rt[0])
        print(
            "stereo baseline: "
            f"input={input_baseline:.4f} m output={estimated_baseline:.4f} m "
            f"delta={estimated_baseline - input_baseline:+.4f} m"
        )
        print(
            "stereo relative rotation: "
            f"input={input_angle:.3f} deg output={estimated_angle:.3f} deg "
            f"delta={estimated_angle - input_angle:+.3f} deg"
        )
        if abs(estimated_baseline - input_baseline) > 0.01 or abs(estimated_angle - input_angle) > 0.5:
            print(
                "warning: estimated stereo geometry changed a lot; this round is likely unstable",
                file=sys.stderr,
            )

    if final_speed > 0.2:
        print(
            "warning: final-state speed is high; stop and hold still 1-2 s before saving calibration",
            file=sys.stderr,
        )
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
